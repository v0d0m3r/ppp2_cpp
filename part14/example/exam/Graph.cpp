void Shape::draw() const
{
    Fl_Color oldfc = fl_color();
    fl_color(lcolor.as_int());              // Задаем цвет
    fl_line_style(ls.style(), ls.width());  // Задаем стиль
    draw_lines();
    fl_color(oldfc);
    fl_line_style(0);
}

